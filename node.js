const addThis = (a, b) => {
  return a + b;
};
const subThis = (a, b) => {
  return a - b;
};
const divThis = (a, b) => {
  return a / b;
};
const multThis = (a, b) => {
  return a * b;
};
console.log(addThis(20, 100));
console.log(subThis(20, 100));
console.log(divThis(20, 100));
console.log(multThis(20, 100));
